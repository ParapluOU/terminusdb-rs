/// Convenience macro for generating a vector of schemas from multiple types.
///
/// This macro takes a list of types that implement `ToTDBSchema` and generates
/// a vector containing their schema definitions. This is useful for bulk schema
/// insertion operations.
///
/// # Examples
/// ```rust,ignore
/// use terminusdb_schema::{schemas, ToTDBSchema};
///
/// #[derive(TerminusDBModel)]
/// struct Person { name: String, age: i32 }
///
/// #[derive(TerminusDBModel)]
/// struct Company { name: String, employees: Vec<Person> }
///
/// // Generate schemas for multiple types
/// let schema_vec = schemas!(Person, Company);
///
/// // Use with client operations
/// client.insert_documents(schema_vec, args).await?;
/// ```
#[macro_export]
macro_rules! schemas {
    ($($schema_type:ty),+ $(,)?) => {
        {
            use $crate::ToTDBSchema;
            vec![
                $(
                    <$schema_type as ToTDBSchema>::to_schema(),
                )+
            ]
        }
    };
    () => {
        vec![]
    };
}

/// Convenience macro for creating a `Vec<Box<dyn ToTDBInstances>>` from multiple instances.
///
/// This macro simplifies seeding operations by automatically boxing each instance.
/// Each expression must implement `ToTDBInstances + Sized + 'static`.
///
/// # Examples
/// ```rust,ignore
/// use terminusdb_schema::{tdbinstances, ToTDBInstances};
///
/// let user = User { name: "Admin".into(), email: "admin@example.com".into() };
/// let projects = vec![
///     Project { title: "Alpha".into(), active: true },
///     Project { title: "Beta".into(), active: false },
/// ];
///
/// // Without macro (verbose):
/// let instances = vec![
///     Box::new(user) as Box<dyn ToTDBInstances>,
///     Box::new(projects) as Box<dyn ToTDBInstances>,
/// ];
///
/// // With macro (clean):
/// let instances = tdbinstances![user, projects];
///
/// // Use with open_database seeder:
/// client.open_database::<(User, Project), _>("mydb", Some(|| {
///     tdbinstances![
///         User { name: "Admin".into(), email: "admin@example.com".into() },
///         vec![project1, project2],
///     ]
/// })).await?;
/// ```
#[macro_export]
macro_rules! tdbinstances {
    ($($expr:expr),* $(,)?) => {
        vec![
            $(
                <_ as $crate::ToTDBInstances>::boxed($expr),
            )*
        ]
    };
}

/// Convenience macro for creating a seeder closure for `open_database`.
///
/// This macro wraps instances in a `Some(|| { ... })` closure structure,
/// eliminating boilerplate when calling `open_database`.
///
/// # Examples
/// ```rust,ignore
/// use terminusdb_schema::tdbseeder;
///
/// // Without macro (verbose):
/// client.open_database::<(User, Project), _>("mydb", Some(|| {
///     vec![
///         Box::new(user) as Box<dyn ToTDBInstances>,
///         Box::new(projects) as Box<dyn ToTDBInstances>,
///     ]
/// })).await?;
///
/// // With macro (clean):
/// client.open_database::<(User, Project), _>("mydb", tdbseeder![
///     user,
///     projects,
/// ]).await?;
///
/// // Complex example with inline construction:
/// client.open_database::<(User, Project), _>("mydb", tdbseeder![
///     User { name: "Admin".into(), email: "admin@example.com".into() },
///     vec![
///         Project { title: "Alpha".into(), active: true },
///         Project { title: "Beta".into(), active: false },
///     ],
/// ]).await?;
/// ```
#[macro_export]
macro_rules! tdbseeder {
    ($($expr:expr),* $(,)?) => {
        Some(|| {
            $crate::tdbinstances![$($expr),*]
        })
    };
}

/// Trait for converting tuples of types into vectors of schemas.
///
/// This trait is implemented for tuples up to 20 elements, where each element
/// must implement `ToTDBSchema`. It provides a type-safe way to generate
/// multiple schemas at compile time.
pub trait ToTDBSchemas {
    /// Convert the tuple of types into a vector of their schemas.
    fn to_schemas() -> Vec<crate::Schema>;
}

/// Trait for enums that can be serialized to/from TerminusDB string values.
///
/// This trait is derived automatically for simple enums (unit variants only)
/// using the `TerminusDBModel` derive macro. It provides bidirectional mapping
/// between Rust enum variants and their TerminusDB string representations.
///
/// # Example
/// ```rust,ignore
/// #[derive(TerminusDBModel)]
/// enum ImportStatus {
///     FullyImported,
///     MetadataOnly,
/// }
///
/// // to_tdb_value converts variant to lowercase string
/// assert_eq!(ImportStatus::FullyImported.to_tdb_value(), "fullyimported");
///
/// // from_tdb_value parses string back to variant
/// assert_eq!(
///     TDBEnum::from_tdb_value("fullyimported"),
///     Some(ImportStatus::FullyImported)
/// );
/// ```
pub trait TDBEnum: Sized {
    /// Returns all variants of this enum.
    fn variants() -> Vec<Self>;

    /// Convert this variant to its TDB string representation.
    /// (e.g., `FullyImported` -> `"fullyimported"`)
    fn to_tdb_value(&self) -> String;

    /// Try to construct enum from TDB string representation.
    /// (e.g., `"fullyimported"` -> `Some(FullyImported)`)
    fn from_tdb_value(s: &str) -> Option<Self>;
}

// Blanket implementation: any ToTDBSchema automatically implements ToTDBSchemas
impl<T: ToTDBSchema> ToTDBSchemas for T {
    fn to_schemas() -> Vec<crate::Schema> {
        T::to_schema_tree()
    }
}

// Macro to generate ToTDBSchemas implementations for tuples
macro_rules! impl_to_tdb_schemas_for_tuple {
    // Base case for single element tuple
    ($T:ident) => {
        impl<$T> ToTDBSchemas for ($T,)
        where
            $T: ToTDBSchema,
        {
            fn to_schemas() -> Vec<crate::Schema> {
                $T::to_schema_tree()
            }
        }
    };

    // Recursive case for multiple elements
    ($($T:ident),+) => {
        impl<$($T),+> ToTDBSchemas for ($($T,)+)
        where
            $($T: ToTDBSchema,)+
        {
            fn to_schemas() -> Vec<crate::Schema> {
                let mut schemas = Vec::new();
                $(schemas.extend($T::to_schema_tree());)+
                schemas
            }
        }
    };
}

